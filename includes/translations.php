/**
 * Get the active language code for the plugin
 */
function bootshas_get_language() {
    // 1. Plugin override setting takes priority
    $override = get_option('bootshas_language', 'en');
    if ($override && $override !== 'auto') {
        return $override;
    }

    // 2. Auto-detect from WordPress locale
    $locale = get_locale(); // e.g. 'lv', 'en_US', 'de_DE', 'ru_RU'
    $lang = substr($locale, 0, 2); // 'lv', 'en', 'de', 'ru'

    $translations = bootshas_get_translations();
    if (isset($translations[$lang])) {
        return $lang;
    }

    return 'en'; // fallback
}

