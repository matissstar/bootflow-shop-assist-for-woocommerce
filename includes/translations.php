/**
 * Get translations for current language
 */
function bootshas_get_strings() {
    $lang = bootshas_get_language();
    $primary_languages = ['en', 'lv', 'de'];
    if (!in_array($lang, $primary_languages, true)) {
        $lang = 'en';
    }
    $all = bootshas_get_translations();
    $strings = $all[$lang] ?? $all['en'];
    return bootshas_apply_free_overrides($lang, $strings);
}

/**
 * Translate a single key using the plugin's own translation system
 */
function bootshas_t($key) {
    static $strings = null;
    if ($strings === null) {
        $strings = bootshas_get_strings();
    }
    return $strings[$key] ?? $key;
}

function bootshas_get_starter_questions() {
    $items = get_option('bootshas_starter_questions', []);
    if (!is_array($items)) return [];
    $lang = bootshas_get_language();
    $items = apply_filters('bootflow_shop_assist_starter_questions_for_language', $items, $lang);
    $result = [];
    foreach ($items as $item) {
        if (!empty($item['question'])) {
            $result[] = ['question' => $item['question'], 'type' => $item['type'] ?? 'text'];
        }
    }
    return $result;
}
